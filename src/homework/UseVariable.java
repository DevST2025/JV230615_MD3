package homework;

public class UseVariable {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.printf("i = %d\n", i);
        System.out.printf("f = %f\n", f);
        System.out.printf("b = %b\n", b);
        System.out.printf("c = %c\n", c);
        System.out.printf("s = %s\n", s);
    }
}
